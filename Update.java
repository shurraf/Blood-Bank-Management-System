    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String donorID = jTextField7.getText();
        String Name = jTextField1.getText();
        String FatherName = jTextField2.getText();
        String MotherName = jTextField3.getText();
        String Gender = jTextField10.getText();
        String DateOfBirth = jTextField11.getText();
        String Mobile = jTextField4.getText();
        String Email = jTextField5.getText();
        String BloodGroup = jTextField8.getText();
        String City = jTextField6.getText();
        String Address = jTextArea1.getText();
        try{
        Connection con = ConnectionProvider.getConnection();
        PreparedStatement pst = con.prepareStatement("UPDATE Donor SET Name=?, FatherName=?, MotherName=?, Gender=?, DateOfBirth=?, Mobile=?, Email=?, BloodGroup=?, City=?, Address=? WHERE donorID=?");
        pst.setString(1, Name);
        pst.setString(2, FatherName);
        pst.setString(3, MotherName);
        pst.setString(4, Gender);
        pst.setString(5, DateOfBirth);
        pst.setString(6, Mobile);
        pst.setString(7, Email);
        pst.setString(8, BloodGroup);
        pst.setString(9, City);
        pst.setString(10, Address);
        pst.setString(11, donorID);
        
        int rowsAffected = pst.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Successfully Updated");
            setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Failed to update record. No matching donor ID found.");
        }
        con.close();
    } catch(SQLException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

