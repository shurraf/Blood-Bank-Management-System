    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String DonorIDStr = jTextField7.getText();
        try{
            int DonorID = Integer.parseInt(DonorIDStr);
            Connection con = ConnectionProvider.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT *FROM Donor WHERE DonorID ='"+DonorID+"'");
            
            if(rs.next()){
                jTextField1.setText(rs.getString(2));
                jTextField2.setText(rs.getString(3));
                jTextField3.setText(rs.getString(4));
                jTextField10.setText(rs.getString(5));
                jTextField11.setText(rs.getString(6));
                jTextField4.setText(rs.getString(7));
                jTextField8.setText(rs.getString(9));
                jTextField6.setText(rs.getString(10));
                jTextField5.setText(rs.getString(8));
                jTextArea1.setText(rs.getString(11));
                jTextField7.setEditable(false);
            }
            else JOptionPane.showMessageDialog(null,"ID doesn't exist");
        }
        catch(Exception e){JOptionPane.showMessageDialog(null,e);}
    }//GEN-LAST:event_jButton4ActionPerformed

